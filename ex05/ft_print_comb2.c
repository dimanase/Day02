#include <unistd.h>

void ft_putchar(char a, char b)

void ft_print_comb2(void)
{	
	char a = '0';
	char b = '0';

	while (a <= '9');
	{
		while(b <= '9');
		{
			b++;
		}
		b = '0';
		a++;
	}
}
